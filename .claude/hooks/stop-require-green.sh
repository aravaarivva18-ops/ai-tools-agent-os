#!/usr/bin/env bash
# Stop hook: детерминированный hard-halt перед завершением хода.
# Сам ПЕРЕПРОГОНЯЕТ полные гейты (а не верит кешу) — иначе дыра: агент коммитит
# (дерево чистое), и проверка «нет незакоммиченного» пропускает ход независимо от того,
# реально ли код зелёный (особенно в /goal-режиме).
#
# ВАЖНО про коды gates.sh:
#   exit 0 → всё зелёное            → не блокируем
#   exit 3 → НЕТ настоящих проверок → НЕ блокируем (фаза планирования / проект не настроен;
#            блокировать нечего — кода на проверку нет; loop.sh при коммите сам не пропустит)
#   иначе  → реальный провал проверки → БЛОКИРУЕМ (exit 2 показывает stderr агенту)
#
# Так планирование (нет package.json → нет проверок) не попадает в луп, но реально
# красный тест/линт по-прежнему не даёт завершить ход.

set -uo pipefail
cd "$(git rev-parse --show-toplevel 2>/dev/null || pwd)"

[[ -x scripts/gates.sh ]] || exit 0

mkdir -p logs
./scripts/gates.sh >> logs/loop.log 2>&1
code=$?

if [[ $code -eq 0 ]]; then
  echo "gates: PASS" > .claude/hooks/.last-gate-status
  exit 0
fi

if [[ $code -eq 3 ]]; then
  # Нечего проверять (планирование / не настроено) — не блокируем ход.
  echo "gates: N/A (нет настоящих проверок)" > .claude/hooks/.last-gate-status
  exit 0
fi

# Реальный провал проверки → блокируем завершение хода.
echo "gates: FAIL" > .claude/hooks/.last-gate-status
echo "Гейты (scripts/gates.sh) красные — реальный провал проверки. Почини причину перед тем, как останавливаться, не обходи гейты. Полный вывод в logs/loop.log." >&2
exit 2
