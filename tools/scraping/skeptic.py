#!/usr/bin/env python3
"""Skeptic — Agent-Powered critical review and architectural auditing tool.

Leverages the executing agent's cognitive capabilities to audit code, content, and business ideas.
"""

import argparse
import os
import sys
from pathlib import Path

# System prompt configurations for each audit mode
SYSTEM_PROMPTS = {
    "content": """# Роль
Ты — жесткий выпускающий редактор, главред с 15-летним опытом и скептик.

# Твоя задача
Проанализируй присланный текст (статью, пост, сценарий). Найди:
1. Воду, канцелярит, пустые штампы и клише, которые не несут пользы.
2. Логические дыры, противоречия, слабые аргументы или бездоказательные заявления.
3. Места, которые написаны слишком сложно, скучно или "по-нейросетевому" (предложи, как сделать живым языком).

# Формат отчета
Напиши отчет строго на русском языке в формате Markdown:
- **Критическая оценка**: (насколько текст убедителен и интересен от 1 до 10)
- **🗑️ Обнаруженный мусор (вода/штампы)**: (конкретные примеры и предложения по сокращению)
- **🔍 Логические и содержательные дыры**: (где автор недоговаривает или противоречит себе)
- **💡 Правки**: список конкретных рекомендаций "Было -> Стало".""",
    "code": """# Роль
Ты — ведущий инженер, архитектор систем и дотошный аудитор безопасности кода.

# Твоя задача
Проанализируй предложенный код. Найди:
1. Потенциальные баги, утечки ресурсов, некорректную обработку ошибок.
2. Проблемы с безопасностью (хардкод ключей, SQL-инъекции, плохая валидация данных).
3. Оверинжиниринг (лишняя сложность, плохая читаемость) и неоптимальные места по памяти/скорости.

# Формат отчета
Напиши отчет строго на русском языке в формате Markdown:
- **Оценка качества кода**: (от 1 до 10, где 10 — идеальный продакшн-код)
- **🚨 Критические баги и уязвимости**: (описание проблемы, файл/строка, почему это опасно)
- **⚠️ Оверинжиниринг и оптимизация**: (где код избыточен или медленен)
- **🛠️ Как исправить**: лаконичные примеры безопасного и простого рефакторинга.""",
    "business": """# Роль
Ты — циничный венчурный инвестор, трекер стартапов и продуктовый стратег.

# Твоя задача
Оцени бизнес-идею, продуктовый оффер или гипотезу. Найди:
1. Скрытые риски, почему клиенты НЕ станут платить (проблема надумана, высокая цена переключения, нет ценности).
2. Дыры в бизнес-модели, unit-экономике или каналах привлечения (наивные ожидания о дешевом трафике).
3. Слабые места в позиционировании и предложении по сравнению с очевидными конкурентами.

# Формат отчета
Напиши отчет строго на русском языке в формате Markdown:
- **Жизнеспособность идеи**: (оценка от 1 до 10, насколько реально на этом заработать)
- **⚠️ Главные причины провала (почему не купят)**: (конкретные барьеры и риски)
- **🎯 Дыры в маркетинге и дистрибуции**: (слабые места в привлечении клиентов)
- **🔄 Что переделать (Пивот)**: 3-4 конкретных шага по изменению оффера для повышения выживаемости идеи.""",
}


def analyze_target(content: str, mode: str) -> str:
    """Delegates the audit request to the executing agent."""
    if os.environ.get("SKEPTIC_TEST_MODE") == "1":
        # Mock analysis for unit test purposes
        if mode == "code":
            return """- **Оценка качества кода**: 8/10
- **🚨 Критические баги и уязвимости**: Нет критических уязвимостей.
- **⚠️ Оверинжиниринг и оптимизация**: Код компактен.
- **🛠️ Как исправить**: Все в порядке."""
        else:
            return """- **Критическая оценка**: 7/10
- **🗑️ Обнаруженный мусор (вода/штампы)**: Нет.
- **🔍 Логические и содержательные дыры**: Нет.
- **💡 Правки**: Все хорошо."""

    system_prompt = SYSTEM_PROMPTS.get(mode, SYSTEM_PROMPTS["content"])

    # Output a structured request block for the agent to intercept and process.
    # We output instructions directly to the terminal where the agent reads the output.
    request_str = f"""
================================================================================
[AGENT SKEPTIC REQUEST]
MODE: {mode}
SYSTEM_PROMPT:
{system_prompt}
--------------------------------------------------------------------------------
CONTENT TO ANALYZE:
{content}
================================================================================
"""
    print(request_str)

    # Inform the agent they need to fulfill this request.
    print("\n[AGY-SKEPTIC] Ожидание генерации вердикта от агента Antigravity...")

    # In interactive/CLI mode, we instruct the agent directly:
    print(
        "Пожалуйста, выполните анализ контента в соответствии с системным промптом режима."
    )

    return "Запрос на скептический анализ успешно передан исполняющему агенту."


def main():
    parser = argparse.ArgumentParser(
        description="Skeptic — Agent-Powered инструмент критической оценки текстов, кода и бизнес-идей."
    )
    parser.add_argument(
        "target", help="Путь к файлу для проверки ИЛИ непосредственно текст в кавычках."
    )
    parser.add_argument(
        "--mode",
        choices=["content", "code", "business"],
        default="content",
        help="Режим проверки: content (текст, блоги), code (аудит кода), business (идеи, офферы). По умолчанию: content.",
    )

    args = parser.parse_args()

    # 1. Read content
    target_path = Path(args.target)
    if target_path.exists() and target_path.is_file():
        try:
            content = target_path.read_text(encoding="utf-8")
            print(f"📄 Читаем файл: {target_path.name} ({args.mode} mode)...")
        except Exception as e:
            print(f"Ошибка при чтении файла {args.target}: {e}")
            sys.exit(1)
    else:
        content = args.target
        print(f"💬 Анализируем введенный текст ({args.mode} mode)...")

    # 2. Trigger audit handoff
    print("🔍 Запуск скептической экспертизы...")
    report = analyze_target(content, args.mode)

    # 3. Output results
    print("\n" + "=" * 40 + " ОТЧЕТ СKEПТИКА " + "=" * 40)
    print(report)
    print("=" * 96 + "\n")


if __name__ == "__main__":
    main()
